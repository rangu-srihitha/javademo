package javademo;

import java.util.Scanner;
public class largestall {
	public static void main(String args[]) {
		Scanner sc=new Scanner(System.in);
		
		
		
		while(true) {
			
			int b=sc.nextInt();
			int max=b;
			if(b==0) {
				break;
			}
			
			if(b>max) {
				System.out.print(b);
			}
			
			
			
		}
		
	}

}
